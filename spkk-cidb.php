        // Add smooth scrolling animation when page loads
        document.addEventListener('DOMContentLoaded', function() {
            // Animate sections on scroll
            const observerOptions = {
                threshold: 0.1,
                rootMargin: '0px 0px -50px 0px'
            };

            const observer = new IntersectionObserver(function(entries) {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        entry.target.style.opacity = '1';
                        entry.target.style.transform = 'translateY(0)';
                    }
                });
            }, observerOptions);

            // Set initial state and observe sections
            document.querySelectorAll('.section').forEach(section => {
                section.style.opacity = '0';
                section.style.transform = 'translateY(30px)';
                section.style.transition = 'all 0.6s ease-out';
                observer.observe(section);
            });

            // Add click animation to requirement list items
            document.querySelectorAll('.requirement-list li, .area-list li').forEach(item => {
                item.addEventListener('click', function() {
                    this.style.transform = 'scale(0.95)';
                    setTimeout(() => {
                        this.style.transform = 'translateX(10px)';
                    }, 150);
                });
            });

            // Add hover effect to table rows
            document.querySelectorAll('tbody tr').forEach(row => {
                row.addEventListener('mouseenter', function() {
                    this.style.transform = 'scale(1.02)';
                });

                row.addEventListener('mouseleave', function() {
                    this.style.transform = 'scale(1)';
                });
            });
        });

        // Add interactive counter animation
        function animateCounters() {
            const gradeElements = document.querySelectorAll('.grade-cell');
            gradeElements.forEach((element, index) => {
                setTimeout(() => {
                    element.style.animation = 'pulse 0.5s ease-in-out';
                }, index * 100);
            });
        }

        // Trigger counter animation on page load
        window.addEventListener('load', () => {
            setTimeout(animateCounters, 1000);
        });
